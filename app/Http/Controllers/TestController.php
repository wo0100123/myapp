     */
    public function store(Request $request)
    {
        //
        // echo '我是test控制器的store';

        //处理文件上传
        if (!$request->hasFile('myfile')) {
            echo '没有上传信息';
            return;
        }

        //设置一个新的名字
        $name = date('YmdHis',time()).rand(1111,9999);

        //获取后缀
        // $suffix = $request->file('myfile')->getClientOriginalExtension();;

        $suffix = $request->file('myfile')->extension();

        $request->file('myfile')->move('./uploads',$name.'.'.$suffix);

        return back();


    }

